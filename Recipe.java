public class Recipe
{
   private String name;
   private String ingredients;
   private String instructions;
   private String information;
   
   public Recipe(){
    name="";
    ingredients="";
    insructions="";
    information="";
   }
   
   public String get_name(){
    return name;
   }
   public String get_ingredients(){
    return ingredients;
   }
   public String get_instructions(){
    return instruction;
    }
   public String get_information(){
    return information;
   }
} 
